public class check_even_or_odd {
    public static void main(String[] args) {
        int num = Integer.parseInt("31"); //args[0]
        if (num%2 == 0){
            System.out.println("The number " + num + " is even number");
        }
        else{
            System.out.println("The number " + num + " is even number");
        }
    }
}
